package Modelo;

public class Comida {

    int IDCOM, PRECIO;
    String COMBO;

    public int getIDCOM() {
        return IDCOM;
    }

    public void setIDCOM(int IDCOM) {
        this.IDCOM = IDCOM;
    }

    public int getPRECIO() {
        return PRECIO;
    }

    public void setPRECIO(int PRECIO) {
        this.PRECIO = PRECIO;
    }

    public String getCOMBO() {
        return COMBO;
    }

    public void setCOMBO(String COMBO) {
        this.COMBO = COMBO;
    }

}
